응용할 수 있어야 하는 것들 : 

1. SUB 함수를 이용해서 word 값을 뺄 수 있다.

2. 올림값을 이용하여 연산결과를 저장할 수 있다.

3. 뺄셈할 때 word에서 큰 값에서 작은값을 뺄 수 있다. (즉, 절대값을 구할 수 있다)

4. 계산결과르 10진수 또는 16진수로 화면에 출력할 수 있다.

